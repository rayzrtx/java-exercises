public class Strings {
    public static void main(String[] args){
        String message = "We don't need no thought control.";
        System.out.println("We don't need no education.\n" + message);
        String message2 = "Check \"this\" out!, \"s inside of \"s!";
        System.out.println(message2);
        String message3 = "In Windows, the main drive is usually C:\\";
        System.out.println(message3);
        String message4 = "I can do back slashes \\, and double back slashes \\\\, and the amazing triple backslash \\\\\\!";
        System.out.println(message4);
    }
}
